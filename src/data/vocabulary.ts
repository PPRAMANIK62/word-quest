// Helper function to generate audio URL
export function getAudioUrl(languageCode: string, lessonSlug: string, word: string): string {
  // Normalize word for filename (remove spaces, special characters)
  const normalizedWord = word.toLowerCase().replace(/[^a-z0-9]/g, "");
  return `/audio/${languageCode}/${lessonSlug}/${normalizedWord}.mp3`;
}

